// ========== While loop ===============
// 1) while loop
// 2) do while loop :- it will run atleast 1 time.

let a = 0;
while (a <= 10) {
    console.log(a);
    a++;
}
console.log(a)

// do while loop :- first {} block will execute then condition will check.
let i = 0
do {
    console.log(i);
    i++;
} while (i < 10);