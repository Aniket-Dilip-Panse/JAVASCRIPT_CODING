// operator in js
let a = 45;
let b = 2;
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("a ** b = ", a ** b);
console.log("a % b = ", a % b);

console.log(`${b} , ${++b} , ${b++}`); // 2 , 3 , 3

console.log("a += 5 ", a += 5);
console.log("a -= 5 ", a -= 5);
console.log("a *= 5 ", a *= 5);
console.log("a /= 5 ", a /= 5);
console.log("a %= 5 ", a %= 5);
console.log("a **= 5 ", a **= 5);


